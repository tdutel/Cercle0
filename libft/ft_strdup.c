#include "libft.h"

char	*ft_strdup(const char *src)
{
	char		*new;
	size_t		i;
	size_t		size;

	size = 0;
	while (src[size])
		size++;
	new = malloc(sizeof(*new) * size + 1);
	if (!new)
		return (NULL);
	i = 0;
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
