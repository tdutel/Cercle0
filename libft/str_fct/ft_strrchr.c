#include "../includes/str_fct.h"

char	*ft_strrchr(char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i])
		i++;
	i += 1;
	while (i--)
		if (s[i] == c % 256)
			return (&s[i]);
	return (NULL);
}
